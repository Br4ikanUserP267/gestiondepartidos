    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        String fecha= JOptionPane.showInputDialog(null, "Ingrese la fecha");
        String hora= JOptionPane.showInputDialog(null, "Ingrese la hora");
        String lugar= JOptionPane.showInputDialog(null, "Ingrese la lugar");
        Iterator<Encuentros >iterador = coleccion.iterator();
        boolean encontrado=  false;
        posicionElementoEncontrado= 0;
        while(iterador.hasNext()){
            Encuentros encuentro = iterador.next();
            if (encuentro.getFecha().equals(fecha) && encuentro.getHora().equals(hora) && encuentro.getLugar().equals(lugar)){
                jTextField14.setText(encuentro.getEquipoLocal());
                jTextField15.setText(encuentro.getEquipoVisitante());
                jTextField155.setText(encuentro.getFecha());
                jTextField16.setText(encuentro.getHora());
                jTextField17.setText(encuentro.getLugar());
                jButton7.setEnabled(true);
                encontrado = true;
  
                break;
            }
            posicionElementoEncontrado++;
        }
        if(encontrado){
            JOptionPane.showMessageDialog(null, "elemento encontrado");
        }else{
            JOptionPane.showMessageDialog(null, "elemento no  encontrado");
        }
        
    }//GEN-LAST:event_jButton9ActionPerformed

