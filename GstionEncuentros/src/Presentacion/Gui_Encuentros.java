    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       if(Equipo1.getText().equals("")   || Equipo2.getText().equals("")||Equipo3.getText().equals("")||Equipo4.getText().equals("")|| Equipo5.getText().equals("")){
           JOptionPane.showMessageDialog(null,"faltan datos por digitar");
           Equipo1.requestFocus();
       }else{
            Encuentros encuentro = new Encuentros();
            encuentro.setEquipoLocal(Equipo1.getText());
            encuentro.setEquipoVisitante(Equipo2.getText());
            encuentro.setFecha(Equipo3.getText());
            encuentro.setHora(Equipo4.getText());
            encuentro.setLugar(Equipo5.getText() );
            coleccion.add(encuentro);
            JOptionPane.showMessageDialog(null,"Registrado con exito");
            Equipo1.setText("");
            Equipo2.setText("");
            Equipo3.setText("");
            Equipo4.setText("");
            Equipo5.setText("");
           
       }
       
       
       
    }//GEN-LAST:event_jButton2ActionPerformed

